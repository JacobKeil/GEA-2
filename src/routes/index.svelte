<div class="flex-end">
  <h2>How can this be problematic?</h2>
</div>
<br>
<div class="flex-reverse">
  <img src="https://m.media-amazon.com/images/G/01/sm/sbg/bangalore-main-image_1x.jpg" alt="Guy playing a game">
  <p>There is always debate if video games are problematic in the long term. A 6 year study by Brigham Young University determined that 10% of video game players exhibited pathological behavior.This behavior includes playing excessively, depression, anxiety, and shyness. There are many studies that games are healthy and can help with reflexes and brain stimulation. But too much of anything can be unhealthy, which is why game addictions can be negative.</p>
