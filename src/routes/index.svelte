<div class="flex-start">
  <h2>What is a gaming disorder?</h2>
</div>
<br>
<div class="flex">
  <p>According the World Health Organization (WHO), as a pattern of gaming behavior characterized by impaired control over gaming, increasing priority given to gaming over other activities to the extent that gaming takes precedence over other interests and daily activities, and continuation or escalation of gaming despite the occurrence of negative consequences. Not all gamers have a disorder, which is why gaming is still loved by billions of people.</p>
